#!/usr/bin/env node
const fs = require('fs');
const path = require('path');

/**
 * Resets the Expo Router `app/` directory.
 *
 * - Moves current `app/` -> `app-example/` (or `app-example-<timestamp>/` if needed)
 * - Recreates a minimal router starter layout (same files Expo Router template expects)
 *
 * Usage:
 *   node ./scripts/reset-project.js
 *   node ./scripts/reset-project.js --dry-run
 */

const projectRoot = path.resolve(__dirname, '..');
const appDir = path.join(projectRoot, 'app');
const appExampleDir = path.join(projectRoot, 'app-example');

const argv = process.argv.slice(2);
const dryRun = argv.includes('--dry-run');

function log(msg) {
  process.stdout.write(`${msg}\n`);
}

function runOp(desc, fn) {
  if (dryRun) {
    log(`[dry-run] ${desc}`);
    return;
  }
  log(desc);
  return fn();
}

function exists(p) {
  try {
    fs.accessSync(p);
    return true;
  } catch {
    return false;
  }
}

if (!exists(appDir)) {
  log(`Error: expected router directory does not exist: ${appDir}`);
  process.exit(1);
}

// Move current app out of the way.
if (exists(appExampleDir)) {
  const ts = new Date().toISOString().replace(/[:.]/g, '-');
  const backupDir = `${appExampleDir}-${ts}`;
  runOp(`Backup existing ${appExampleDir} -> ${backupDir}`, () => {
    fs.renameSync(appExampleDir, backupDir);
  });
}

runOp(`Move ${appDir} -> ${appExampleDir}`, () => {
  fs.renameSync(appDir, appExampleDir);
});

// Recreate starter `app/` structure.
runOp(`Create ${appDir}/...`, () => {
  fs.mkdirSync(appDir, { recursive: true });
  fs.mkdirSync(path.join(appDir, '(tabs)'), { recursive: true });
});

